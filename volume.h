void vlkubus(int s){
	printf(" Volume kubus = %d\n", s * s * s );
}

void vlbalok(int p, int l, int t){
	printf(" Volume balok = %d\n", p * l * t );
}

void vltabung(int r, int t2){
	printf(" Volume tabung = %d\n", 3.14 * r * r * t2);
}
